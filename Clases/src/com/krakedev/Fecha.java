package com.krakedev;

public class Fecha {
	private int anio;
	private int mes;
	private int día;
	
	public Fecha(int mes) {
		this.mes=mes;
	}
	
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getDía() {
		return día;
	}
	public void setDía(int día) {
		this.día = día;
	}	
	
	
}
